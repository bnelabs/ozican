/** Planet keys in order from the Sun */
export const PLANET_ORDER = ['sun', 'mercury', 'venus', 'earth', 'mars', 'jupiter', 'saturn', 'uranus', 'neptune'];

/** Comparison data for the compare table */
export function getComparisonData() {
  return PLANET_ORDER.filter(k => k !== 'sun').map(key => {
    const p = SOLAR_SYSTEM[key];
    return {
      name: p.name,
      type: p.type,
      diameter: p.physicalAttributes.diameter,
      mass: p.massEarths + '× Earth',
      gravity: p.gravity,
      dayLength: p.dayLength,
      year: p.orbitalPeriod,
      moons: p.knownMoons !== undefined ? p.knownMoons : p.moons.length,
      temperature: p.temperature,
      distance: p.distanceFromSun,
    };
  });
}
